<h1>Content of Notices</h1>
<p>The DMCA requires that such notices of alleged copyright infringement be in writing. In order for the University to take any action on the basis of your notice, the written communication must include the following information:<p1>

<p>Identification of the copyrighted work or works claimed to have been infringed.  If multiple works are allegedly being infringed at a single site, please provide a representative list of such works.</p>
<p>A description of the material that is claimed to be infringing, and information sufficient to permit Columbia’s agent to locate the material.
Contact information for you, including your address, telephone number, and/or email address.
A statement by you that you have a good faith belief that the use of the material in the manner complained of is not authorized by the copyright owner, the owner’s agent, or by the law.</p>
<p>A statement by you that the information in the notification is accurate, and under penalty of perjury, you are authorized on behalf of the owner to enforce the copyrights that are claimed to be infringed.</p>
<p>A physical or electronic signature of the copyright owner or the  person authorized to act on behalf of the rights owner.</p>
<p>The University will not be able to act promptly on your complaint, or at all, if you do not provide all of the information listed above.</p>

